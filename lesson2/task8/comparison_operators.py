"""
Python tiene muchos tipos de operadores de comparacion, incluidos
>=,
<=,
>,
<, etc.
Todas las operaciones de comparacion en Python tienen la misma prioridad.
Las comparaciones arrojan valores booleanos: True o False.
Las comparaciones pueden encadenarse arbitrariamente.

"""
print("::::::::::::::::::::::::::::::::::::::::::::::::::::::::::")
one = 1
two = 2
three = 3

print(one < two < three)

is_greater = three > two
print(is_greater)

print("-------------------------------------------------------")

uno = 1
dos = 2
tres = 3

print(uno < dos < tres)
verdadero = dos > uno
falso = tres < uno
print(verdadero)
print(falso)
