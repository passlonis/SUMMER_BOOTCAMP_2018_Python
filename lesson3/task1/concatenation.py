"""
La combinacion de dos cadenas usando el simbolo + se llama concatenacion.
"""
print("::::::::::::::::::::::::::::::::::::::::::::::::::::::::::")
hello = "Hola"
world = 'Mundo'

hello_world = hello + " " + world
print(hello_world)

print("----------------------------------------------------------")

hola = "hola"
mundo = "mundo"

hola_mundo = hola + mundo
print(hola_mundo)
print(hola + " " + mundo)
