"""
Si desea verificar si una cadena contiene una letra especifica o una subcadena
puede usar la palabra reservada in.
"""
print("::::::::::::::::::::::::::::::::::::::::::::::::::::::::::")

helado = "ice cream"
print("cream" in helado)

contains = "ice" in helado
print(contains)

